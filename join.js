// join() method convert array to string
let flowers = ['lily', 'rose', 'tulip', 'dalia', 'daffodil', 'dolon'];
console.log(flowers);

let flowersName = flowers.join();
console.log(flowersName);

let flowersName2 = flowers.join('');
console.log(flowersName2);

let flowersName3 = flowers.join(' ');
console.log(flowersName3);

let flowersName4 = flowers.join('-');
console.log(flowersName4);

let flowersName5 = flowers.join(',');
console.log(flowersName5);

let flowersName6 = flowers.join('|');
console.log(flowersName6);